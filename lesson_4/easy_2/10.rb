=begin

Because there are so many benefits to using OOP we will just summarize some of
the major ones:

    Creating objects allows programmers to think more abstractly about the code
    they are writing.

    Objects are represented by nouns so are easier to
    conceptualize.

    It allows us to only expose functionality to the parts of
    code that need it, meaning namespace issues are much harder to come across.

    It allows us to easily give functionality to different parts of an
    application without duplication.

    We can build applications faster as we can
    reuse pre-written code.

    As the software becomes more complex this
    complexity can be more easily managed.

